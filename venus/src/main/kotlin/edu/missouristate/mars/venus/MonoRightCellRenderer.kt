package edu.missouristate.mars.venus

import java.awt.Component
import java.awt.Font
import javax.swing.JLabel
import javax.swing.JTable
import javax.swing.SwingConstants
import javax.swing.table.DefaultTableCellRenderer

class MonoRightCellRenderer : DefaultTableCellRenderer() {
    companion object {
        @JvmField val monospacedFont = Font("Monospaced", Font.PLAIN, 12)
    }

    override fun getTableCellRendererComponent(
        table: JTable?,
        value: Any?,
        isSelected: Boolean,
        hasFocus: Boolean,
        row: Int,
        column: Int
    ): Component {
        val cell = super.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, column) as JLabel
        cell.font = monospacedFont
        cell.horizontalAlignment = SwingConstants.RIGHT
        return cell
    }
}